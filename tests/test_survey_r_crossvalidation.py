CS_SCENARIOS = ["cs_weighted_nocov", "cs_weighted_cov"]


class TestCSWeighted:
    """Cross-validate CallawaySantAnna weighted ATT vs R did::att_gt."""

    def _fit_scenario(self, r_results, key):
        r = r_results[key]
        data = _load_scenario_data(r)

        covariates = None
        if r.get("has_covariates"):
            covariates = ["x1"]

        sd = SurveyDesign(weights="weight")

        est = CallawaySantAnna(
            estimation_method="reg",
            control_group="never_treated",
            base_period="varying",
            n_bootstrap=0,
        )
        result = est.fit(
            data, "outcome", "unit", "period", "first_treat",
            covariates=covariates,
            aggregate="simple",
            survey_design=sd,
        )
        return result, r

    @pytest.mark.parametrize("key", CS_SCENARIOS)
    def test_att_matches_r_did(self, r_results, key):
        """Group-time ATTs should match R's att_gt with weightsname."""
        if key not in r_results:
            pytest.skip(f"Scenario {key} not in R results (did package not available)")
        result, r = self._fit_scenario(r_results, key)

        py_gt = sorted(result.group_time_effects.items())
        r_atts = r["gt_att"]

        assert len(py_gt) == len(r_atts), (
            f"{key}: Python has {len(py_gt)} GT cells, R has {len(r_atts)}"
        )

        for i, ((g, t), eff) in enumerate(py_gt):
            _assert_close(
                eff["effect"], r_atts[i],
                rtol=1e-3, atol=0.05,
                label=f"{key} ATT(g={g},t={t})",
            )

    @pytest.mark.parametrize("key", CS_SCENARIOS)
    def test_overall_att_matches(self, r_results, key):
        """Simple-aggregated overall ATT should match R."""
        if key not in r_results:
            pytest.skip(f"Scenario {key} not in R results")
        result, r = self._fit_scenario(r_results, key)

        _assert_close(
            result.overall_att, r["overall_att"],
            rtol=1e-3, atol=0.05,
            label=f"{key} overall ATT",
        )

    @pytest.mark.parametrize("key", CS_SCENARIOS)
    def test_se_is_design_based(self, r_results, key):
        """Python SE should be positive and finite (design-based)."""
        if key not in r_results:
            pytest.skip(f"Scenario {key} not in R results")
        result, _ = self._fit_scenario(r_results, key)

        assert np.isfinite(result.overall_se), "Overall SE should be finite"
        assert result.overall_se > 0, "Overall SE should be positive"

    @pytest.mark.parametrize("key", CS_SCENARIOS)
    def test_design_se_differs_from_naive(self, r_results, key):
        """Design-based SE should differ from naive (unweighted) SE."""
        if key not in r_results:
            pytest.skip(f"Scenario {key} not in R results")
        r = r_results[key]
        data = _load_scenario_data(r)

        covariates = ["x1"] if r.get("has_covariates") else None

        # With survey weights
        sd = SurveyDesign(weights="weight")
        est_survey = CallawaySantAnna(
            estimation_method="reg", control_group="never_treated",
            base_period="varying", n_bootstrap=0,
        )
        result_survey = est_survey.fit(
            data, "outcome", "unit", "period", "first_treat",
            covariates=covariates, aggregate="simple", survey_design=sd,
        )

        # Without survey weights
        est_naive = CallawaySantAnna(
            estimation_method="reg", control_group="never_treated",
            base_period="varying", n_bootstrap=0,
        )
        result_naive = est_naive.fit(
            data, "outcome", "unit", "period", "first_treat",
            covariates=covariates, aggregate="simple",
        )

        assert result_survey.overall_se != result_naive.overall_se, (
            f"{key}: Design-based SE ({result_survey.overall_se:.6f}) should "
            f"differ from naive SE ({result_naive.overall_se:.6f})"
        )


