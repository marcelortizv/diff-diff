"""Cross-validation tests: diff-diff survey variance vs R's survey package.

Compares ATT, SE, df, and CI from diff-diff's Taylor Series Linearization
(TSL) variance against pre-computed golden values from R's authoritative
``survey::svyglm()`` (Lumley 2004).

Tier 1: DifferenceInDifferences + svyglm under 4 design variants + covariates + 3 seeds
Tier 2: CallawaySantAnna + did::att_gt with survey weights (ATT only)
Tier 3: BRR replicate weights via LinearRegression + svrepdesign

JSON fixtures generated by: benchmarks/R/benchmark_survey_crossvalidation.R
"""

import json
from pathlib import Path

import numpy as np
import pandas as pd
import pytest

from diff_diff import CallawaySantAnna, DifferenceInDifferences, SurveyDesign
from diff_diff.twfe import TwoWayFixedEffects

BENCHMARK_DIR = Path(__file__).parent.parent / "benchmarks" / "data" / "synthetic"
RESULTS_FILE = BENCHMARK_DIR / "survey_crossvalidation_r_results.json"

# Tolerances
ATT_RTOL = 1e-4
ATT_ATOL = 0.01
SE_RTOL = 0.01  # 1%
SE_ATOL = 0.05
CI_RTOL = 0.01
CI_ATOL = 0.05


def _assert_close(py_val, r_val, rtol, atol, label):
    """Assert values are close with informative error message."""
    if np.isnan(r_val):
        return
    diff = abs(py_val - r_val)
    threshold = max(atol, rtol * abs(r_val))
    assert diff < threshold, (
        f"{label}: Python={py_val:.6f}, R={r_val:.6f}, "
        f"diff={diff:.6f}, threshold={threshold:.6f} "
        f"(rtol={rtol}, atol={atol})"
    )


@pytest.fixture(scope="module")
def r_results():
    """Load pre-computed R golden values."""
    if not RESULTS_FILE.exists():
        pytest.skip(
            f"R benchmark file not found: {RESULTS_FILE}. "
            "Run: Rscript benchmarks/R/benchmark_survey_crossvalidation.R"
        )
    with open(RESULTS_FILE) as f:
        return json.load(f)


def _load_scenario_data(scenario):
    """Extract DataFrame from a scenario's embedded data dict."""
    data = scenario["data"]
    df = pd.DataFrame(
        {
            col: vals
            for col, vals in data.items()
        }
    )
    # Handle R's Inf serialization (jsonlite writes Inf as "Inf" string)
    if "first_treat" in df.columns:
        ft = pd.to_numeric(df["first_treat"], errors="coerce")
        ft = ft.fillna(0)  # "Inf" -> NaN via to_numeric -> 0
        ft = ft.replace(np.inf, 0)
        df["first_treat"] = ft.astype(int)

    # Ensure correct dtypes
    int_cols = ["unit", "period", "treated", "post", "stratum", "psu"]
    for col in int_cols:
        if col in df.columns:
            df[col] = df[col].astype(int)
    float_cols = ["outcome", "weight", "fpc", "x1", "x2"]
    for col in float_cols:
        if col in df.columns:
            df[col] = df[col].astype(float)
    return df


def _build_survey_design(design_type, has_fpc=True):
    """Construct the SurveyDesign for a given design type."""
    if design_type == "strata_psu_fpc":
        return SurveyDesign(
            weights="weight", strata="stratum", psu="psu",
            fpc="fpc" if has_fpc else None,
        )
    elif design_type == "strata_psu_nofpc":
        return SurveyDesign(weights="weight", strata="stratum", psu="psu")
    elif design_type == "weights_only":
        return SurveyDesign(weights="weight")
    elif design_type == "strata_only":
        return SurveyDesign(weights="weight", strata="stratum")
    else:
        raise ValueError(f"Unknown design type: {design_type}")


# ---------------------------------------------------------------------------
# Tier 1: svyglm DiD — DifferenceInDifferences vs R
# ---------------------------------------------------------------------------

TIER1_SCENARIOS = [
    # Design variants at seed=42
    "strata_psu_fpc_s42",
    "strata_psu_nofpc_s42",
    "weights_only_s42",
    "strata_only_s42",
    # Covariate variant
    "strata_psu_fpc_cov_s42",
    # Multi-seed robustness
    "strata_psu_fpc_s123",
    "strata_psu_fpc_s789",
]


class TestSvyglmDiD:
    """Cross-validate DifferenceInDifferences survey variance vs R svyglm."""

    def _fit_scenario(self, r_results, key):
        """Fit diff-diff DiD for a given scenario and return result."""
        r = r_results[key]
        data = _load_scenario_data(r)
        sd = _build_survey_design(r["design_type"])

        covariates = None
        if r.get("has_covariates"):
            covariates = ["x1", "x2"]

        est = DifferenceInDifferences()
        result = est.fit(
            data, "outcome", "treated", "post",
            covariates=covariates,
            survey_design=sd,
        )
        return result, r

    @pytest.mark.parametrize("key", TIER1_SCENARIOS)
    def test_att_matches_r(self, r_results, key):
        result, r = self._fit_scenario(r_results, key)
        _assert_close(result.att, r["att"], ATT_RTOL, ATT_ATOL,
                       f"{key} ATT")

    @pytest.mark.parametrize("key", TIER1_SCENARIOS)
    def test_se_matches_r(self, r_results, key):
        result, r = self._fit_scenario(r_results, key)
        _assert_close(result.se, r["se"], SE_RTOL, SE_ATOL,
                       f"{key} SE")

    @pytest.mark.parametrize("key", TIER1_SCENARIOS)
    def test_df_matches_r(self, r_results, key):
        result, r = self._fit_scenario(r_results, key)
        py_df = result.survey_metadata.df_survey
        r_df = r["df"]
        assert py_df == r_df, (
            f"{key} df: Python={py_df}, R={r_df}"
        )

    @pytest.mark.parametrize("key", TIER1_SCENARIOS)
    def test_ci_matches_r(self, r_results, key):
        result, r = self._fit_scenario(r_results, key)
        _assert_close(result.conf_int[0], r["ci_lower"], CI_RTOL, CI_ATOL,
                       f"{key} CI lower")
        _assert_close(result.conf_int[1], r["ci_upper"], CI_RTOL, CI_ATOL,
                       f"{key} CI upper")

    @pytest.mark.parametrize("key", TIER1_SCENARIOS)
    def test_fpc_reduces_se(self, r_results, key):
        """FPC scenario should have smaller SE than non-FPC equivalent."""
        if key != "strata_psu_fpc_s42":
            pytest.skip("Only applies to strata_psu_fpc_s42")
        r_fpc = r_results["strata_psu_fpc_s42"]
        r_nofpc = r_results["strata_psu_nofpc_s42"]
        assert r_fpc["se"] < r_nofpc["se"], (
            f"FPC SE ({r_fpc['se']:.6f}) should be < non-FPC SE ({r_nofpc['se']:.6f})"
        )


