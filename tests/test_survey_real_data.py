    def test_a4_twfe(self, api_results):
        """A4: TWFE plumbing with strata + FPC + weights."""
        r = api_results["a4_twfe"]
        data = self._load_api_data(api_results)

        sd = SurveyDesign(weights="pw", strata="stype", fpc="fpc")
        est = TwoWayFixedEffects()
        result = est.fit(
            data, "outcome", "treated", "period", "school_id",
            survey_design=sd,
        )

        # ATT should match R's pooled DiD. SE differs because TWFE absorbs
        # unit fixed effects (within-transform), giving a smaller residual
        # variance than R's svyglm(outcome ~ treated * post).
        _assert_close(result.att, r["att"], ATT_RTOL, ATT_ATOL, "A4 TWFE ATT")
        assert np.isfinite(result.se), "A4 TWFE SE should be finite"
        assert result.se > 0, "A4 TWFE SE should be positive"

    def test_a5_subpopulation_elementary(self, api_results):
        """A5: Subpopulation — elementary schools only."""
        r = api_results["a5_subpop_elementary"]
