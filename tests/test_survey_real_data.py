class TestApiSchoolAccountability:
    """Cross-validate survey DiD on real API data vs R survey::svyglm.

    Policy: California PSAA (1999) — Governor's Performance Award eligibility
    as treatment, api99/api00 as pre/post scores. Stratified by school type
    (E/M/H) with FPC and probability weights from apistrat sample.
    """

    def _load_api_data(self, api_results):
        """Load embedded API panel data."""
        df = _load_embedded_data(api_results)
        # Ensure dtypes
        for col in ["school_id", "period", "post", "treated"]:
            df[col] = df[col].astype(int)
        for col in ["outcome", "pw", "fpc", "meals", "ell"]:
            if col in df.columns:
                df[col] = pd.to_numeric(df[col], errors="coerce")
        return df

    def test_a1_strata_fpc_weights(self, api_results):
        """A1: Full TSL with strata + FPC + weights."""
        r = api_results["a1_strata_fpc_weights"]
        data = self._load_api_data(api_results)

        sd = SurveyDesign(weights="pw", strata="stype", fpc="fpc")
        est = DifferenceInDifferences()
        result = est.fit(data, "outcome", "treated", "post", survey_design=sd)

        _assert_close(result.att, r["att"], ATT_RTOL, ATT_ATOL, "A1 ATT")
        _assert_close(result.se, r["se"], SE_RTOL, SE_ATOL, "A1 SE")
        assert result.survey_metadata.df_survey == r["df"], (
            f"A1 df: Python={result.survey_metadata.df_survey}, R={r['df']}"
        )
        _assert_close(result.conf_int[0], r["ci_lower"], CI_RTOL, CI_ATOL,
                       "A1 CI lower")
        _assert_close(result.conf_int[1], r["ci_upper"], CI_RTOL, CI_ATOL,
                       "A1 CI upper")

    def test_a2_strata_no_fpc(self, api_results):
        """A2: TSL with strata + weights, no FPC."""
        r = api_results["a2_strata_weights"]
