class TestRecsReplicateWeights:
    """Cross-validate JK1 replicate weight variance on real RECS 2020 data.

    RECS is a federal energy survey by EIA with 60 pre-computed JK1 replicate
    weights. This validates the replicate weight variance path with real
    federal survey weights using a weighted regression (not a full DiD).
    """

    def _load_recs_data(self, recs_results):
        """Load embedded RECS subset data."""
        df = _load_embedded_data(recs_results)
        # Ensure numeric columns
        for col in ["TOTALBTU", "DOLLAREL", "NWEIGHT"]:
            if col in df.columns:
                df[col] = pd.to_numeric(df[col], errors="coerce")
        # Identify replicate weight columns
        rep_cols = sorted([c for c in df.columns if c.startswith("NWEIGHT")
                           and c != "NWEIGHT"])
        for col in rep_cols:
            df[col] = pd.to_numeric(df[col], errors="coerce")
        return df, rep_cols

    def test_c1_simple_regression(self, recs_results):
        """C1: Simple regression TOTALBTU ~ KOWNRENT with JK1 replicate SEs."""
        r = recs_results["c1_simple"]
        data, rep_cols = self._load_recs_data(recs_results)
        data = data.dropna(subset=["TOTALBTU", "KOWNRENT", "NWEIGHT"]).reset_index(
            drop=True
        )

        # Build design matrix: intercept + KOWNRENT
        from diff_diff.linalg import LinearRegression as LR

        X = data[["KOWNRENT"]].values.astype(float)
        y = data["TOTALBTU"].values.astype(float)

        sd = SurveyDesign(
            weights="NWEIGHT",
            replicate_weights=rep_cols,
            replicate_method="JK1",
        )
        resolved = sd.resolve(data)

        reg = LR(include_intercept=True, survey_design=resolved)
        reg.fit(X, y)

        # KOWNRENT coefficient is at index 1 (intercept=0, KOWNRENT=1)
        py_coef = reg.coefficients_[1]
        py_se = np.sqrt(reg.vcov_[1, 1])

        _assert_close(py_coef, r["coef"], ATT_RTOL, ATT_ATOL, "C1 coef")
        _assert_close(py_se, r["se"], REP_SE_RTOL, REP_SE_ATOL, "C1 SE")
        assert reg.survey_df_ == r["df"], (
            f"C1 df: Python={reg.survey_df_}, R={r['df']}"
        )
        # Compute CI from coef, SE, and survey df
        from scipy.stats import t as t_dist
        t_crit = t_dist.ppf(0.975, reg.survey_df_)
        py_ci_lower = py_coef - t_crit * py_se
        py_ci_upper = py_coef + t_crit * py_se
        _assert_close(py_ci_lower, r["ci_lower"], REP_CI_RTOL, REP_CI_ATOL,
                       "C1 CI lower")
        _assert_close(py_ci_upper, r["ci_upper"], REP_CI_RTOL, REP_CI_ATOL,
                       "C1 CI upper")

    def test_c2_full_regression(self, recs_results):
        """C2: Full regression TOTALBTU ~ KOWNRENT + TYPEHUQ + REGIONC."""
        r = recs_results["c2_full"]
        data, rep_cols = self._load_recs_data(recs_results)
        data = data.dropna(
            subset=["TOTALBTU", "KOWNRENT", "TYPEHUQ", "REGIONC", "NWEIGHT"]
        ).reset_index(drop=True)

        from diff_diff.linalg import LinearRegression as LR

        # Create dummy variables for categorical predictors
        X_parts = [data[["KOWNRENT"]].values.astype(float)]
        # TYPEHUQ dummies (drop first level)
        typehuq_dummies = pd.get_dummies(data["TYPEHUQ"], prefix="TYPEHUQ", drop_first=True)
        X_parts.append(typehuq_dummies.values.astype(float))
        # REGIONC dummies (drop first level)
        regionc_dummies = pd.get_dummies(data["REGIONC"], prefix="REGIONC", drop_first=True)
        X_parts.append(regionc_dummies.values.astype(float))

        X = np.column_stack(X_parts)
        y = data["TOTALBTU"].values.astype(float)

        sd = SurveyDesign(
            weights="NWEIGHT",
            replicate_weights=rep_cols,
            replicate_method="JK1",
        )
        resolved = sd.resolve(data)

        reg = LR(include_intercept=True, survey_design=resolved)
        reg.fit(X, y)

        # KOWNRENT coefficient is at index 1
        py_coef = reg.coefficients_[1]
        py_se = np.sqrt(reg.vcov_[1, 1])

        _assert_close(py_coef, r["coef_kownrent"], ATT_RTOL, ATT_ATOL,
                       "C2 KOWNRENT coef")
        _assert_close(py_se, r["se_kownrent"], REP_SE_RTOL, REP_SE_ATOL,
                       "C2 KOWNRENT SE")
        assert reg.survey_df_ == r["df"], (
            f"C2 df: Python={reg.survey_df_}, R={r['df']}"
        )
        from scipy.stats import t as t_dist
        t_crit = t_dist.ppf(0.975, reg.survey_df_)
        py_ci_lower = py_coef - t_crit * py_se
        py_ci_upper = py_coef + t_crit * py_se
        _assert_close(py_ci_lower, r["ci_lower_kownrent"], REP_CI_RTOL, REP_CI_ATOL,
                       "C2 CI lower")
        _assert_close(py_ci_upper, r["ci_upper_kownrent"], REP_CI_RTOL, REP_CI_ATOL,
                       "C2 CI upper")

    def test_c3_deff_diagnostics(self, recs_results):
        """C3: DEFF diagnostics from real JK1 replicate SEs.

        DEFF = (survey_SE / naive_SE)^2. The naive SE baseline differs between
        R (weighted lm) and Python (compute_deff_diagnostics SRS baseline), so
        DEFF values aren't directly comparable. This test verifies DEFFs are
        finite, positive, and > 1 (expected for a complex survey design).
        """
        data, rep_cols = self._load_recs_data(recs_results)
        data = data.dropna(subset=["TOTALBTU", "KOWNRENT", "NWEIGHT"]).reset_index(
            drop=True
        )

        from diff_diff.linalg import LinearRegression as LR

        X = data[["KOWNRENT"]].values.astype(float)
        y = data["TOTALBTU"].values.astype(float)

        sd = SurveyDesign(
            weights="NWEIGHT",
            replicate_weights=rep_cols,
            replicate_method="JK1",
        )
        resolved = sd.resolve(data)

        reg = LR(include_intercept=True, survey_design=resolved)
        reg.fit(X, y)

        # Compute DEFF diagnostics
        from diff_diff.survey import compute_deff_diagnostics

        deff = compute_deff_diagnostics(
            X=np.column_stack([np.ones(len(y)), X]),
            residuals=y - reg.predict(X),
            survey_vcov=reg.vcov_,
            weights=resolved.weights,
        )

        # DEFF should be finite and positive for all coefficients
        assert all(np.isfinite(deff.deff)), "DEFF values should be finite"
        assert all(deff.deff > 0), "DEFF values should be positive"
        assert all(np.isfinite(deff.effective_n)), "Effective n should be finite"
        assert all(deff.effective_n > 0), "Effective n should be positive"

        # DEFF > 1 expected for a complex survey with unequal weights
        assert deff.deff[1] > 1.0, (
            f"DEFF(KOWNRENT) = {deff.deff[1]:.4f}, expected > 1.0 for survey data"
        )
