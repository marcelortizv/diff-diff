"""Cross-validation: diff-diff survey variance vs R using real survey data.

Complements test_survey_r_crossvalidation.py (synthetic data) with real-world
survey datasets to validate numerical agreement between diff-diff and R's
authoritative ``survey`` package.

Suite A: API dataset — TSL variance (California school accountability, PSAA 1999)
Suite B: NHANES — TSL with strata+PSU (ACA young adult coverage provision)
Suite C: RECS 2020 — JK1 replicate weight variance

NOTE: Suite A uses observational program assignment (Governor's Performance Award
eligibility) as the treatment indicator. Suite B uses the ACA dependent coverage
provision (age 19-25 vs 27-34). These are real policies but the DiD estimates
should be interpreted with caution. The primary purpose is validating that
diff-diff's survey variance matches R, not causal claims.

JSON fixtures generated by:
  benchmarks/R/benchmark_realdata_api.R
  benchmarks/R/benchmark_realdata_nhanes.R
  benchmarks/R/benchmark_realdata_recs.R
"""

import json
from pathlib import Path

import numpy as np
import pandas as pd
import pytest

from diff_diff import DifferenceInDifferences, SurveyDesign
from diff_diff.twfe import TwoWayFixedEffects

BENCHMARK_DIR = Path(__file__).parent.parent / "benchmarks" / "data" / "real"

# Golden value files
API_RESULTS_FILE = BENCHMARK_DIR / "api_realdata_golden.json"
NHANES_RESULTS_FILE = BENCHMARK_DIR / "nhanes_realdata_golden.json"
RECS_RESULTS_FILE = BENCHMARK_DIR / "recs_realdata_golden.json"

# Tight tolerances — observed gaps are < 1e-10, so 1e-8 guards against
# regressions without being so loose that a real bug could slip through.
ATT_RTOL = 1e-8
ATT_ATOL = 1e-8
SE_RTOL = 1e-8
SE_ATOL = 1e-8
CI_RTOL = 1e-8
CI_ATOL = 1e-8

# Replicate weight methods: still tight (observed gaps < 1e-10)
REP_SE_RTOL = 1e-8
REP_SE_ATOL = 1e-8
REP_CI_RTOL = 1e-8
REP_CI_ATOL = 1e-8

# Mark for easy filtering: pytest -m realdata
pytestmark = pytest.mark.realdata


def _assert_close(py_val, r_val, rtol, atol, label):
    """Assert values are close with informative error message."""
    if np.isnan(r_val):
        return
    diff = abs(py_val - r_val)
    threshold = max(atol, rtol * abs(r_val))
    assert diff < threshold, (
        f"{label}: Python={py_val:.6f}, R={r_val:.6f}, "
        f"diff={diff:.6f}, threshold={threshold:.6f} "
        f"(rtol={rtol}, atol={atol})"
    )


def _load_embedded_data(results, key="_data"):
    """Load the embedded dataset from golden JSON."""
    data = results[key]
    df = pd.DataFrame({col: vals for col, vals in data.items()})
    return df


